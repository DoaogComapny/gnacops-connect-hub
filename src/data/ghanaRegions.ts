// Helper function to get districts by region
export const getDistrictsByRegion = (regionName: string): string[] => {
  const region = ghanaRegions.find(r => r.name === regionName);
  return region ? region.districts : [];
};

// Helper function to get all region names
export const getAllRegions = (): string[] => {
  return ghanaRegions.map(r => r.name);
};
